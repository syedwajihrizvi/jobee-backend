DROP TABLE IF EXISTS interviews;

CREATE TABLE interviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id BIGINT NOT NULL,
    candidate_id BIGINT NOT NULL,
    interviewer_id BIGINT NOT NULL,
    scheduled_time TIMESTAMP NOT NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'SCHEDULED',
    description TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (job_id) REFERENCES jobs(id) ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (candidate_id) REFERENCES user_profiles(id) ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (interviewer_id) REFERENCES business_accounts(id) ON DELETE RESTRICT ON UPDATE CASCADE
);